
# Constant Expressions
x = 15 + 1.3
  
print("constant expression value:")
print(x)
print('\n')


# Arithmetic Expressions
x = 100
y = 50
  
add = x + y
sub = x - y
pro = x * y
div = x / y
print("arithmetic expression value:")
print(add)
print(sub)
print(pro)
print(div)
print('\n')

